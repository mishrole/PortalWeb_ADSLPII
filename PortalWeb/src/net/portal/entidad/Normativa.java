public class Normativa {
	private int normativa_id;
	private String normativa_nombre;
	
	public int getNormativa_id() {
		return normativa_id;
	}
	public void setNormativa_id(int normativa_id) {
		this.normativa_id = normativa_id;
	}
	public String getNormativa_nombre() {
		return normativa_nombre;
	}
	public void setNormativa_nombre(String normativa_nombre) {
		this.normativa_nombre = normativa_nombre;
	}
}
